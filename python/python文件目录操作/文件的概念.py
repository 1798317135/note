
#########################################  文件的概念  ##########################################
#
#
# --- 1.0 文件是指数据存放的容器 
# 
# --- 2.0 文件可以永久的储存数据内容
# 
# ---3.0 文件的有三部分组成
#        文件名称
#           --- 文件名在同级目录里 不允许有相同的文件名称存在
#        扩招名称
#           --- 扩展名 有很多 .text .jpeg .avi 等等
#               根据不同的扩展名 来区别文件类型 和 解析方式
#        文件内容
#           --- 有文本文件 
#               可以储存文本等内容
#           
#           ---- 二进制文件
#               储存图片，视频，音乐等内容