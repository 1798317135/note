# 1.0 面向对象的设计原则 SOLID
# 
# --- S (Single Responsibility Principle)
#     1.0 单一的职责
#         一个类只负责一项职责
#     2.0 好处 
#         易于维护，写出高内聚的代码
#         易于代码服用
# --- O (Open Close principle)
#      2.0 开放封闭原则
#      3.0  开放 只指 对扩展开放
#      4.0 封闭 是指 对修改关闭
#      5.0  好处易于 维护，有利于 扩展性 和 安全性
